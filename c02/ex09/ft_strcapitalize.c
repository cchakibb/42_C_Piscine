char	*ft_strcapitalize(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[0] >= 97 && str[0] <= 122)
			str[0] -= 32;
		if (str[i] == ' ' && (str[i + 1] >= 97 && str[i + 1] <= 122))
			str[i + 1] -= 32;
		if (str[i] == '-' || str[i] == '+')
			str[i + 1] -= 32;
		i++;
	}
	return (str);
}
